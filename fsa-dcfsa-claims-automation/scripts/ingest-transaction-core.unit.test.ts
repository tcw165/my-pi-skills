import { describe, it, expect } from "vitest";
import { COLLECTION_OPTIONS, INDEXES, isValidTransactionDate } from "./ingest-transaction-core.js";

// ---------------------------------------------------------------------------
// COLLECTION_OPTIONS
// ---------------------------------------------------------------------------

describe("COLLECTION_OPTIONS structure", () => {
  const schema = COLLECTION_OPTIONS.validator!.$jsonSchema as Record<string, unknown>;
  const properties = schema.properties as Record<string, Record<string, unknown>>;
  const required = schema.required as string[];

  it("has a $jsonSchema validator with bsonType object", () => {
    expect(schema).toBeDefined();
    expect(schema.bsonType).toBe("object");
  });

  it("validationLevel is strict", () => {
    expect(COLLECTION_OPTIONS.validationLevel).toBe("strict");
  });

  it("validationAction is error", () => {
    expect(COLLECTION_OPTIONS.validationAction).toBe("error");
  });

  it("additionalProperties is true (allows _id and future fields)", () => {
    expect(schema.additionalProperties).toBe(true);
  });

  it("required array contains all 15 expected field names", () => {
    const expectedFields = [
      "date",
      "merchant",
      "amount",
      "description",
      "category",
      "issuer",
      "card_last_four",
      "imported_at",
      "source_file",
      "eligibility_reason",
      "eligible_fsa",
      "eligible_dcfsa",
      "eligible_fsa_confidence",
      "eligible_dcfsa_confidence",
      "eligibility_scored_at",
    ];
    expect(required).toHaveLength(15);
    for (const field of expectedFields) {
      expect(required).toContain(field);
    }
  });

  it("required array does not contain _id (auto-generated by MongoDB)", () => {
    expect(required).not.toContain("_id");
  });

  it("date field enforces YYYY-MM-DD pattern", () => {
    expect(properties.date.pattern).toBe("^\\d{4}-\\d{2}-\\d{2}$");
    expect(properties.date.bsonType).toBe("string");
  });

  it("card_last_four field enforces 4-digit pattern", () => {
    expect(properties.card_last_four.pattern).toBe("^\\d{4}$");
    expect(properties.card_last_four.bsonType).toBe("string");
  });

  it("amount is bsonType double", () => {
    expect(properties.amount.bsonType).toBe("double");
  });

  it("eligible_fsa_confidence is bounded 0–1", () => {
    expect(properties.eligible_fsa_confidence.minimum).toBe(0);
    expect(properties.eligible_fsa_confidence.maximum).toBe(1);
  });

  it("eligible_dcfsa_confidence is bounded 0–1", () => {
    expect(properties.eligible_dcfsa_confidence.minimum).toBe(0);
    expect(properties.eligible_dcfsa_confidence.maximum).toBe(1);
  });

  it("all eligibility fields are nullable (bsonType arrays containing 'null')", () => {
    const nullableFields = [
      "eligibility_reason",
      "eligible_fsa",
      "eligible_dcfsa",
      "eligible_fsa_confidence",
      "eligible_dcfsa_confidence",
      "eligibility_scored_at",
    ];
    for (const field of nullableFields) {
      const bsonType = properties[field].bsonType as string[];
      expect(Array.isArray(bsonType), `${field}.bsonType should be an array`).toBe(true);
      expect(bsonType, `${field} should allow null`).toContain("null");
    }
  });

  it("imported_at is bsonType date (not nullable)", () => {
    expect(properties.imported_at.bsonType).toBe("date");
  });
});

