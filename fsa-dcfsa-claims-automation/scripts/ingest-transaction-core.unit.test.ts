describe("isValidTransactionDate", () => {
  it("accepts a well-formed date", () => {
    expect(isValidTransactionDate("2025-02-14")).toBe(true);
  });

  it("accepts edge dates with syntactically valid digits (regex only, not range)", () => {
    // Month 13 is invalid calendar-wise, but the regex is format-only.
    expect(isValidTransactionDate("2025-13-01")).toBe(true);
  });

  it("rejects a date with a 2-digit year", () => {
    expect(isValidTransactionDate("25-02-14")).toBe(false);
  });

  it("rejects slash-separated dates", () => {
    expect(isValidTransactionDate("2025/02/14")).toBe(false);
  });

  it("rejects an empty string", () => {
    expect(isValidTransactionDate("")).toBe(false);
  });

  it("rejects single-digit month or day", () => {
    expect(isValidTransactionDate("2025-2-4")).toBe(false);
  });

  it("rejects US-format dates (MM-DD-YYYY)", () => {
    expect(isValidTransactionDate("02-14-2025")).toBe(false);
  });
});
