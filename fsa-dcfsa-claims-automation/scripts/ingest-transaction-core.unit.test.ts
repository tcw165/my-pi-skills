describe("INDEXES structure", () => {
  it("has exactly 6 index definitions", () => {
    expect(INDEXES).toHaveLength(6);
  });

  it("all index names are unique strings", () => {
    const names = INDEXES.map((i) => i.name);
    expect(new Set(names).size).toBe(names.length);
    for (const name of names) {
      expect(typeof name).toBe("string");
      expect(name.length).toBeGreaterThan(0);
    }
  });

  it("imported_at_desc has key { imported_at: -1 }", () => {
    const idx = INDEXES.find((i) => i.name === "imported_at_desc");
    expect(idx).toBeDefined();
    expect(idx!.key).toEqual({ imported_at: -1 });
  });

  it("card_last_four has key { card_last_four: 1 }", () => {
    const idx = INDEXES.find((i) => i.name === "card_last_four");
    expect(idx).toBeDefined();
    expect(idx!.key).toEqual({ card_last_four: 1 });
  });

  it("date_desc has key { date: -1 }", () => {
    const idx = INDEXES.find((i) => i.name === "date_desc");
    expect(idx).toBeDefined();
    expect(idx!.key).toEqual({ date: -1 });
  });

  it("eligible_fsa index has sparse: true", () => {
    const idx = INDEXES.find((i) => i.name === "eligible_fsa");
    expect(idx).toBeDefined();
    expect(idx!.sparse).toBe(true);
  });

  it("eligible_dcfsa index has sparse: true", () => {
    const idx = INDEXES.find((i) => i.name === "eligible_dcfsa");
    expect(idx).toBeDefined();
    expect(idx!.sparse).toBe(true);
  });

  it("imported_at_eligible_fsa is a composite index with two keys", () => {
    const idx = INDEXES.find((i) => i.name === "imported_at_eligible_fsa");
    expect(idx).toBeDefined();
    expect(Object.keys(idx!.key)).toHaveLength(2);
    expect(idx!.key).toMatchObject({ imported_at: -1, eligible_fsa: 1 });
  });

  it("no index has unique: true (deduplication is intentionally absent)", () => {
    for (const idx of INDEXES) {
      expect(idx.unique).toBeUndefined();
    }
  });

  it("every index key direction is 1 or -1", () => {
    for (const idx of INDEXES) {
      for (const [field, dir] of Object.entries(idx.key)) {
        expect(
          [1, -1],
          `index "${idx.name}" field "${field}" has invalid direction`,
        ).toContain(dir);
      }
    }
  });
});

